public class increment {
  
        public static void main(String[] args) {
            int var1 = 5, var2 = 5;
            int inc=++var1;
            int dec=--var2;
    
          
            System.out.println("increment="+inc);
    
        
            System.out.println("decrement="+dec);
        }
    }
    


